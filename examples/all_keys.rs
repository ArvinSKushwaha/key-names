use scancode_convert::*;

fn main() {
    let keymap = get_keymap().expect("failed to get keymap");
    for &key in ALL_KEYS {
        println!("{:<15} -> {:?}", format!("{:?}", key), keymap.key_name(key),);
    }
    println!();
    println!(
        "Modifiers: {}<key>",
        keymap.display_mods(true, true, true, true),
    );
}

